package com.chainsys.day3;

public class UnaryOperator {

	public static void main(String[] args) {
		
		//Unary Increment
		int i = 0;
		System.out.println(i);
		System.out.println(i++);
		int x = i++;
		System.out.println(x);
		System.out.println(++x);
		int y = ++x;
		System.out.println(y);
	}
}
